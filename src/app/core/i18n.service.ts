export type TKey = keyof typeof DICT['fr'];

@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly lang = signal<Lang>((localStorage.getItem('tjr_lang') as Lang) || 'fr');
  readonly t = computed(() => DICT[this.lang()]);

  setLang(l: Lang): void {
    this.lang.set(l);
    localStorage.setItem('tjr_lang', l);
  }
}
